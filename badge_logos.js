Badge_logos.prototype.get_SQL = function(cardcode,eventcode) {

    var event_specific_sql =this.get_SQL_Specific(cardcode);
    var sql = `SELECT 
    case sprache when 'IT' then 1  when 'EN' then 3 else 0 end as sprache,
    B.cardcode,B.eventcode,B.klass,B.badge_firma as firma,B.badge_funktion as funktion,B.bp_id,CS.nachname,CS.vorname 
    FROM buchungdaten B 
    left join Clients CS on B.cardcode=CS.cardcode
    WHERE B.eventcode = '${eventcode}'
    and B.cardcode in (${cardcode});
    
    /* retive breakout-Session */
    select cardcode,(select kurz from artikel where itemcode=auftragen_warenkorb.itemcode and kategory in (1,2)) as kurz from auftragen_warenkorb where artikel_typ='B'
    and cardcode in (${cardcode}) and canceled='N' and eventcode= '${eventcode}'
    and (select kurz from artikel where itemcode=auftragen_warenkorb.itemcode and kategory in (1,2)) is not null;
    
    /* take Badge data as Klass text and relative backgroud and text color */
    select badge as klass, midle_txt,color,text_farbe from klassen where eventcode= '${eventcode}';

    ${event_specific_sql}
    `;
    return sql
}

Badge_logos.prototype.get_SQL_Specific = function(cardcode) {
    var event_specific_sql=[];
    event_specific_sql['SIF']=`select antwort as singlequestion,cardcode,
    (select kurz from amadeus_anmeldung.structure_language where element='ICHBINEIN' and sprache= (select case sprache when 'FR' then 2 when 'EN' then 3 else 0 end from clients where cardcode=clients_singlequestion.cardcode)) as ICH_BIN_EIN
    from clients_singlequestion where cardcode in (${cardcode}) and frage='ICH_BIN_EIN' and event_typ='${this.event_typ}'`;
    
    event_specific_sql['ECS']=`select cardcode,
    (select case teilnehmerprotickets when 3 then '#ffffff'  when 2 then '#b2ef5a' when 1 then '#8be5f5' else '#ffffff' end
    from artikel
    where itemcode=auftragen_warenkorb.itemcode) as farbe 
    from auftragen_warenkorb
    where artikel_typ in ('A','M')
    and eventcode   = '${this.eventcode}'
    and cardcode    in (${cardcode})
    and canceled    = 'N'
    and (select obligatorisch from artikel where itemcode=auftragen_warenkorb.itemcode )='Y'`;
   

    var returnwert=event_specific_sql[this.event_typ];

    if(returnwert==''){
        returnwert='No sql specific for this Event is set ' ;
    }
    return returnwert
}


